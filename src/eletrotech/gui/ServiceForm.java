    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Service service = new Service();
        
        service.setName(nameField.getText());
        service.setDescription(descField.getText());
        service.setValue(valueField.getText());
        int disponibility = disponibilitySelect.getSelectedItem().toString().equals("Disponivel") ? 1 : 0; 
        service.setDisponibility(disponibility);
        
        try {
            if(API.createService(service)) {
                showCard("Services", new Services());
            }
        } catch (IOException ex) {
            Logger.getLogger(ServiceForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


